package Vehicle;

public class Vehicle {
    int coordX;
    int coordY;
    int price;
    int speed;
    int year;

    public Vehicle(){}

    public Vehicle(int coordX, int coordY, int price, int speed, int year) {
        this.coordX = coordX;
        this.coordY = coordY;
        this.price = price;
        this.speed = speed;
        this.year = year;
    }
}
